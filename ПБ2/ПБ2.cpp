#include <iostream>
#include <list>

using namespace std;

int main() {
    setlocale(LC_ALL, "Russian");
    double num1, num2;

    // Запрос ввода двух вещественных чисел
    cout << "Введите два вещественных числа: ";
    cin >> num1 >> num2;

    list<double> values; // Создаем список для хранения значений

    values.push_back(num1); // Добавляем первое введенное число в список
    values.push_back(num2); // Добавляем второе введенное число в список

    // Формирование последующих значений списка
    while (true) {
        double new_value = values.back() * values.front(); // Произведение двух последних элементов
        values.push_back(new_value); // Добавляем новое значение в список

        // Проверка на выход за пределы допустимых значений double
        if (new_value == 0 || new_value == HUGE_VAL || new_value == -HUGE_VAL) {
            cout << "Произошло переполнение. Результаты больше не могут храниться в типе double." << endl;
            values.pop_back(); // Удаляем последнее значение, так как оно недопустимо
            break;
        }
    }

    // Вывод значений списка
    cout << "Список значений:" << endl;
    for (double val : values) {
        cout << val << " ";
    }
    cout << endl;

    return 0;
}
